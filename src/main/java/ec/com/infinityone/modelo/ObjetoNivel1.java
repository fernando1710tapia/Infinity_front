package ec.com.infinityone.modelo;

/**
 *
 * @author HP
 */
public class ObjetoNivel1 {
    
    private String codigo;
    private String nombre;
    private String activo;
    private String usuario;
    private String abreviacion;
    private String objRelacionado;
    

    public ObjetoNivel1() {
    }
    
    public String getCodigo() {
        return codigo;
    }

    public void setCodigo(String codigo) {
        this.codigo = codigo;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getActivo() {
        return activo;
    }

    public void setActivo(String activo) {
        this.activo = activo;
    }

    public String getUsuario() {
        return usuario;
    }

    public void setUsuario(String usuario) {
        this.usuario = usuario;
    }

    public String getAbreviacion() {
        return abreviacion;
    }

    public void setAbreviacion(String abreviacion) {
        this.abreviacion = abreviacion;
    }

    public String getObjRelacionado() {
        return objRelacionado;
    }

    public void setObjRelacionado(String objRelacionado) {
        this.objRelacionado = objRelacionado;
    }
    
    
}
